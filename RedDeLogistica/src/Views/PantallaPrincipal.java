    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // DIJKSTRA - Ruta mas barata
        String origen = (String) jComboBox1.getSelectedItem();
        String destino = (String) jComboBox2.getSelectedItem();
        if (origen == null || destino == null) {
            jTextArea1.setText("Seleccione una ciudad de origen y destino.");
            return;
        }
        if (origen.equals(destino)) {
            jTextArea1.setText("El origen y el destino son la misma ciudad.");
            return;
        }
        Dijkstra<String> dijkstra = new Dijkstra<>(grafo);
        dijkstra.ejecutarDijkstra(origen, destino);
        double costo = dijkstra.getCostoMinimo(destino);
        if (costo == Double.MAX_VALUE) {
            jTextArea1.setText("No existe ninguna ruta entre " + origen + " y " + destino + ".");
            return;
        }
        List<String> ruta = dijkstra.getRutaOptima(destino);
        StringBuilder sb = new StringBuilder();
        sb.append("RUTA MAS BARATA (Dijkstra)\n");
        sb.append("==============================\n\n");
        sb.append("Origen: ").append(origen).append("\n");
        sb.append("Destino: ").append(destino).append("\n\n");
        sb.append("Ruta optima:\n  ");
        for (int i = 0; i < ruta.size(); i++) {
            sb.append(ruta.get(i));
            if (i < ruta.size() - 1) {
                sb.append(" -> ");
            }
        }
        sb.append("\n\nCosto Total de Envio: ").append(costo).append(" Bs.");
        jTextArea1.setText(sb.toString());
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // KRUSKAL - Red minima
        Kruskal<String> kruskal = new Kruskal<>(grafo);
        GrafoPesado<String> mst = kruskal.getGrafoAuxiliar();
        StringBuilder sb = new StringBuilder();
        sb.append("RED MINIMA DE CONEXION (Kruskal)\n");
        sb.append("==================================\n\n");
        sb.append("Rutas seleccionadas para conectar\n");
        sb.append("todas las ciudades al menor costo:\n\n");
        Set<String> aristasImpresas = new HashSet<>();
        for (int i = 0; i < mst.cantidadDeVertice(); i++) {
            String origen = mst.getVerticePorNro(i);
            for (String destino : mst.adyacentesDelVertice(origen)) {
                String clave;
                if (origen.compareTo(destino) < 0) {
                    clave = origen + "-" + destino;
                } else {
                    clave = destino + "-" + origen;
                }
                if (!aristasImpresas.contains(clave)) {
                    aristasImpresas.add(clave);
                    double peso = mst.pesoEntreDosVertices(origen, destino);
                    sb.append("  * ").append(origen).append(" <-> ").append(destino);
                    sb.append(" : ").append(peso).append(" Bs.\n");
                }
            }
        }
        sb.append("\nCosto Minimo Total: ").append(kruskal.getCostoTotal()).append(" Bs.");
        jTextArea1.setText(sb.toString());
    }//GEN-LAST:event_jButton3ActionPerformed

