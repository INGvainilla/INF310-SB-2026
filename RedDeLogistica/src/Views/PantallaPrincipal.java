        try {
            precio = Double.parseDouble(jTextField4.getText().trim());
            cantidad = Integer.parseInt(jTextField5.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese precio y cantidad válidos.");
            return;
        }

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del producto.");
            return;
        }

        gestorVentas.registrarProducto(nombre, precio, cantidad);
        
        // Actualizar combo de productos
        jComboBox3.removeAllItems();
        for (Models.Producto p : gestorVentas.getCatalogo()) {
            jComboBox3.addItem(p.getNombre());
        }

        jTextArea1.setText("Producto '" + nombre + "' registrado con éxito.\nPrecio: " + precio + " Bs. | Stock: " + cantidad);
        jTextField6.setText("");
        jTextField4.setText("");
